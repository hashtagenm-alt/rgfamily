-- 사용자 랭킹
CREATE OR REPLACE FUNCTION public.get_user_rank(
  p_user_id uuid, p_season_id bigint DEFAULT NULL
)
RETURNS TABLE(rank integer, total_amount bigint)
LANGUAGE plpgsql STABLE SECURITY DEFINER SET search_path = public AS $$
BEGIN
  IF p_user_id IS NULL THEN RETURN; END IF;
  IF p_season_id IS NULL THEN
    RETURN QUERY
      WITH totals AS (
        SELECT donor_id, sum(amount)::bigint AS total_amount,
               dense_rank() OVER (ORDER BY sum(amount) DESC) AS rank
        FROM public.donations WHERE donor_id IS NOT NULL GROUP BY donor_id
      )
      SELECT totals.rank::integer, totals.total_amount FROM totals WHERE totals.donor_id = p_user_id;
  ELSE
    RETURN QUERY
      WITH totals AS (
        SELECT donor_id, sum(amount)::bigint AS total_amount,
               dense_rank() OVER (ORDER BY sum(amount) DESC) AS rank
        FROM public.donations WHERE donor_id IS NOT NULL AND season_id = p_season_id GROUP BY donor_id
      )
      SELECT totals.rank::integer, totals.total_amount FROM totals WHERE totals.donor_id = p_user_id;
  END IF;
END;
$$;

-- 활성 시즌 랭킹
CREATE OR REPLACE FUNCTION public.get_user_rank_active_season(p_user_id uuid)
RETURNS TABLE(rank integer, total_amount bigint, season_id bigint)
LANGUAGE plpgsql STABLE SECURITY DEFINER SET search_path = public AS $$
DECLARE v_season_id bigint;
BEGIN
  SELECT public.get_active_season_id() INTO v_season_id;
  IF v_season_id IS NULL OR p_user_id IS NULL THEN RETURN; END IF;
  RETURN QUERY
    WITH totals AS (
      SELECT d.donor_id, sum(d.amount)::bigint AS total_amount,
             dense_rank() OVER (ORDER BY sum(d.amount) DESC) AS rank
      FROM public.donations d WHERE d.donor_id IS NOT NULL AND d.season_id = v_season_id GROUP BY d.donor_id
    )
    SELECT totals.rank::integer, totals.total_amount, v_season_id FROM totals WHERE totals.donor_id = p_user_id;
END;
$$;

-- 에피소드 랭킹
CREATE OR REPLACE FUNCTION public.get_episode_rankings(p_episode_id bigint, p_limit integer DEFAULT 50)
RETURNS TABLE(rank integer, donor_id uuid, donor_name text, total_amount bigint)
LANGUAGE plpgsql STABLE SECURITY DEFINER SET search_path = public AS $$
BEGIN
  RETURN QUERY
    WITH totals AS (
      SELECT d.donor_id, d.donor_name, sum(d.amount)::bigint AS total_amount,
             dense_rank() OVER (ORDER BY sum(d.amount) DESC) AS rank
      FROM public.donations d WHERE d.episode_id = p_episode_id AND d.amount > 0 GROUP BY d.donor_id, d.donor_name
    )
    SELECT totals.rank::integer, totals.donor_id, totals.donor_name, totals.total_amount
    FROM totals WHERE totals.rank <= p_limit ORDER BY totals.rank;
END;
$$;

-- VIP 체크
CREATE OR REPLACE FUNCTION public.is_vip_user(user_id uuid)
RETURNS boolean LANGUAGE plpgsql STABLE SECURITY DEFINER SET search_path = public AS $$
DECLARE v_rank integer;
BEGIN
  IF user_id IS NULL THEN RETURN FALSE; END IF;
  IF EXISTS (
    SELECT 1 FROM public.profiles WHERE id = user_id AND role IN ('vip', 'moderator', 'admin', 'superadmin')
  ) THEN RETURN TRUE; END IF;
  SELECT rank INTO v_rank FROM public.get_user_rank(user_id, NULL) LIMIT 1;
  RETURN v_rank IS NOT NULL AND v_rank <= 50;
END;
$$;

-- VIP for episode
CREATE OR REPLACE FUNCTION public.is_vip_for_episode(p_user_id uuid, p_episode_id bigint)
RETURNS boolean LANGUAGE plpgsql STABLE SECURITY DEFINER SET search_path = public AS $$
DECLARE v_rank integer;
BEGIN
  IF p_user_id IS NULL THEN RETURN FALSE; END IF;
  IF EXISTS (SELECT 1 FROM public.profiles WHERE id = p_user_id AND role IN ('vip', 'moderator', 'admin', 'superadmin'))
  THEN RETURN TRUE; END IF;
  SELECT rank INTO v_rank FROM (
    WITH totals AS (
      SELECT d.donor_id, dense_rank() OVER (ORDER BY sum(d.amount) DESC) AS rank
      FROM public.donations d WHERE d.episode_id = p_episode_id AND d.amount > 0 GROUP BY d.donor_id
    ) SELECT totals.rank FROM totals WHERE totals.donor_id = p_user_id
  ) sub LIMIT 1;
  RETURN v_rank IS NOT NULL AND v_rank <= 50;
END;
$$;

