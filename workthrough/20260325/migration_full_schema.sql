-- Total Rankings Public (시청자 점수 변환, total_amount 숨김)
CREATE OR REPLACE VIEW public.total_rankings_public AS
SELECT
  tdr.id,
  tdr.rank,
  tdr.donor_name,
  (tdr.total_amount * 50) AS viewer_score,
  tdr.donation_count,
  tdr.top_bj,
  tdr.donor_id AS profile_id,
  tdr.avatar_url,
  CASE
    WHEN p.id IS NOT NULL
      AND p.avatar_url IS NOT NULL
      AND EXISTS (
        SELECT 1 FROM public.vip_personal_messages vpm
        WHERE vpm.vip_profile_id = p.id AND vpm.is_public = true AND vpm.is_deleted = false
      )
    THEN true
    ELSE false
  END AS is_vip_clickable
FROM public.total_donation_rankings tdr
LEFT JOIN public.profiles p ON tdr.donor_id = p.id;

-- Season Rankings Public
CREATE OR REPLACE VIEW public.season_rankings_public AS
SELECT
  sdr.id,
  sdr.season_id,
  sdr.rank,
  sdr.donor_name,
  (sdr.total_amount * 50) AS viewer_score,
  sdr.donation_count,
  sdr.unit::text,
  sdr.top_bj,
  sdr.donor_id AS profile_id,
  sdr.avatar_url,
  CASE
    WHEN p.id IS NOT NULL
      AND p.avatar_url IS NOT NULL
      AND EXISTS (
        SELECT 1 FROM public.vip_personal_messages vpm
        WHERE vpm.vip_profile_id = p.id AND vpm.is_public = true AND vpm.is_deleted = false
      )
    THEN true
    ELSE false
  END AS is_vip_clickable,
  sdr.updated_at,
  sdr.created_at
FROM public.season_donation_rankings sdr
LEFT JOIN public.profiles p ON sdr.donor_id = p.id;

-- VIP Clickable Profiles
CREATE OR REPLACE VIEW public.vip_clickable_profiles AS
