-- 좋아요 수 증가/감소
CREATE OR REPLACE FUNCTION public.increment_post_like_count(p_post_id bigint, p_delta integer)
RETURNS integer LANGUAGE plpgsql SECURITY DEFINER AS $$
DECLARE new_count integer;
BEGIN
  UPDATE public.posts SET like_count = GREATEST(0, like_count + p_delta) WHERE id = p_post_id
  RETURNING like_count INTO new_count;
  RETURN COALESCE(new_count, 0);
END;
$$;

