-- 시즌 랭킹 원자적 교체
CREATE OR REPLACE FUNCTION public.atomic_replace_season_rankings(p_season_id bigint, p_rankings jsonb)
RETURNS integer LANGUAGE plpgsql SECURITY DEFINER AS $$
DECLARE inserted_count integer;
BEGIN
  DELETE FROM public.season_donation_rankings WHERE season_id = p_season_id;
  INSERT INTO public.season_donation_rankings (season_id, rank, donor_id, donor_name, total_amount, donation_count, top_bj, unit, avatar_url)
  SELECT p_season_id, (r->>'rank')::integer, (r->>'donor_id')::uuid, r->>'donor_name',
         (r->>'total_amount')::bigint, COALESCE((r->>'donation_count')::integer, 0),
         r->>'top_bj', (r->>'unit')::public.org_unit, r->>'avatar_url'
  FROM jsonb_array_elements(p_rankings) AS r;
  GET DIAGNOSTICS inserted_count = ROW_COUNT;
  RETURN inserted_count;
END;
$$;

-- 총 랭킹 원자적 교체
CREATE OR REPLACE FUNCTION public.atomic_replace_total_rankings(p_rankings jsonb)
RETURNS integer LANGUAGE plpgsql SECURITY DEFINER AS $$
