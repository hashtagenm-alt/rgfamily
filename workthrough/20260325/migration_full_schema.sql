BEGIN
  DELETE FROM public.total_donation_rankings;
  INSERT INTO public.total_donation_rankings (rank, donor_id, donor_name, total_amount, donation_count, top_bj, avatar_url)
  SELECT (r->>'rank')::integer, (r->>'donor_id')::uuid, r->>'donor_name',
         (r->>'total_amount')::bigint, COALESCE((r->>'donation_count')::integer, 0),
         r->>'top_bj', r->>'avatar_url'
  FROM jsonb_array_elements(p_rankings) AS r;
  GET DIAGNOSTICS inserted_count = ROW_COUNT;
  RETURN inserted_count;
END;
$$;

